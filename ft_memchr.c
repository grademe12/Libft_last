#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	cc;
	unsigned char	*cs;
	size_t			i;

	cc = (unsigned char)c;
	cs = (unsigned char *)s;
	i = 0;
	while (i < n)
	{
		if (*(cs + i) == cc)
			return ((void *)cs + i);
		else
			i++;
	}
	return (NULL);
}
